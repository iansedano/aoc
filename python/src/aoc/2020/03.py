lines = input.split("\n")



def trees(lines, v_diff, h_diff):

	line_length = len(lines[0])
	total_lines = len(lines)
	last_index = line_length - 1

	v_p = 0
	h_p = 0

	trees_encountered = 0

	iterations = 0

	while v_p != total_lines:
		iterations += 1
		if iterations > 1000:
			raise "too many iterations"
		current_position = lines[v_p][h_p]
		if current_position == "#":
			trees_encountered += 1
		elif current_position == ".":
			pass
		else:
			raise "something went wrong"

		v_p += v_diff
		if v_p >= total_lines:
			break

		h_p = h_p + h_diff
		if h_p > last_index:
			h_p = h_p % line_length

	return trees_encountered

print(
	trees(lines, 1, 1) *
	trees(lines, 1, 3) *
	trees(lines, 1, 5) *
	trees(lines, 1, 7) *
	trees(lines, 2, 1)
)
